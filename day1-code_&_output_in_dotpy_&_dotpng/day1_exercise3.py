# Type Conversion
umur_str = "15"             # Ini string
umur_int = int(umur_str)    # Convert ke integer

print("Umur (string):", umur_str)
print("Umur (integer):", umur_int)
print("Umur 5 tahun lagi:", umur_int + 5)

# Coba Convert Angka Ke String
angka = 100

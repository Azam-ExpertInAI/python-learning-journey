# Calculator Sederhana
angka1 = 10
angka2 = 3

# Hitung Semua Operasi
tambah = angka1 + angka2
kurang = angka1 - angka2
kali = angka1 * angka2
bagi = angka1 / angka2

print(f"{angka1} + {angka2} = {tambah}")
print(f"{angka1} - {angka2} = {kurang}")
print(f"{angka1} * {angka2} = {kali}")
print(f"{angka1} / {angka2} = {bagi}")
